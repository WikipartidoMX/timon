package timon.entities.showcase;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Lob;
import static javax.persistence.FetchType.LAZY;
import javax.persistence.ManyToOne;

/**
 *
 * @author Alfonso TamÃ©s
 */

@Entity
public class SlideImage implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    
    @ManyToOne
    private Slide slide;
    
    @Lob @Basic(fetch=LAZY)
    private byte[] file;    

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof SlideImage)) {
            return false;
        }
        SlideImage other = (SlideImage) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "timon.entities.showcase.Imagen[ id=" + id + " ]";
    }

    /**
     * @return the slide
     */
    public Slide getSlide() {
        return slide;
    }

    /**
     * @param slide the slide to set
     */
    public void setSlide(Slide slide) {
        this.slide = slide;
    }

    /**
     * @return the file
     */
    public byte[] getFile() {
        return file;
    }

    /**
     * @param file the file to set
     */
    public void setFile(byte[] file) {
        this.file = file;
    }
    
}
